<script context="module">
    export async function load({params}) {
        const id = params.id;
        const url = `https://pokeapi.co/api/v2/pokemon/${id}`;
        const res = await fetch(url);
        const pokeman = await res.json();
        return {props: {pokeman}};
    }
</script>
<script>
    export let pokeman;
    const type = pokeman.types[0].type.name;
</script>

<div class="flex flex-col items-center h-screen bg-blue-200">
    <h1 class="font-press-start text-4xl text-center my-8 uppercase">{pokeman.name}</h1>

    <p class="font-press-start">
        Type: <strong>{type}</strong> | Height: <strong>{pokeman.height}</strong> | Weight: <strong>{pokeman.weight}</strong>
    </p>

    <img class="card-image my-6 h-40 w-40" src={pokeman.sprites['front_default']} alt={pokeman.name}>
</div>